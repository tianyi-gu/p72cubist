_TABLES = {
    "P": _PAWN_TABLE,
    "N": _KNIGHT_TABLE,
    "B": _BISHOP_TABLE,
    "R": _ROOK_TABLE,
    "Q": _QUEEN_TABLE,
}


def piece_position(board: Board, color: str) -> float:
    """Sum of piece-square table bonuses. Positive = good for color."""
    score = 0.0
    for row in range(8):
        for col in range(8):
            piece = board.get_piece((row, col))
            if piece is None or piece.upper() == "K":
                continue
            pt = piece_type(piece)
            table = _TABLES.get(pt)
            if table is None:
                continue
            if piece_color(piece) == color:
                # Own piece: use table directly
                score += table[row][col]
            else:
                # Opponent piece: mirror row and subtract
                score -= table[7 - row][col]
    return score
